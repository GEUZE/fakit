#include <sciopta.h>
#include <ossys/libsc.h>

int dectoi(const char *s)
{
  int cnv = 0;
  int n = 9 ;
  while( n && ( *s >= '0' && *s <= '9' ) ){
    cnv *= 10;
    cnv += (*s++)-'0';
    --n;
  }
  return cnv;
}
